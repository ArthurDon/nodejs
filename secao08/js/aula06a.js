/*
As configurações de estilos via JavaScript seguem os mesmos
conceitos e atributos utilizados via CSS.

OBS: Diferença

Em CSS, onde usa-se background-color
Em JS, usa-se backgroundColor

Outro exemplo:

Em CSS, onde usa-se font-size
Em JS, usa-se fontSize

Mais um exemplo:
Em CSS, onde usa-se margin-bottom
Em JS, usa-se marginBottom
*/